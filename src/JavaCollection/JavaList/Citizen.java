package JavaCollection.JavaList;

public class Citizen
{

    private String name;
    private int age;

    // Constructor
    public Citizen(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getters
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
